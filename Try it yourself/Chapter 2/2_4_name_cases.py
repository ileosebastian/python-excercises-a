"""
Store a person’s name in a variable, and then print that person’s name in 
lowercase, uppercase, and titlecase.
"""
# I store in a variable, called person_name, my name. 
person_name = "leo intriago"

print (person_name.lower()) # I print the content the variable as lower case
print (person_name.upper()) # I print the content the variable as upper case
print (person_name.title()) # I print the content the variable as a title
